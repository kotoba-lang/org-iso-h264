(ns h264.encode-test
  "Tests for `h264.encode` (ADR-2607122000 Migration step 8: encode-side
   量子化→変換→CAVLC→簡易モード決定, luma-only Intra_16x16, see that
   namespace's docstring for full scope). Two kinds of test here:

   1. Round-trip through THIS REPO's own `h264.decode` (fast, runs in CI,
      no external tools needed) — verifies the encoder produces a
      self-consistent bitstream its own decoder can parse, with bounded
      reconstruction error.
   2. Fixed fixtures (`encode-*.h264`) generated by `h264.encode` ONCE and
      independently verified, at development time, to be decodable by REAL
      ffmpeg (8.1.1) BIT-EXACT against ffmpeg's own decode of the same
      file — see each fixture's test docstring for the exact verification
      command. Same \"golden vector, ffmpeg-validated\" discipline
      `h264.decode`'s own tests use, checking the OPPOSITE direction (our
      encode → real ffmpeg decode).

   Development-time note, kept for traceability rather than deleted: while
   building this encoder, two apparent decode-side gaps were hit (a
   macroblock referencing a neighbor with real AC content, and any single
   macroblock whose AC coefficients push the neighbor-derived nC to 2 or
   higher). Both turned out to be manifestations of the SAME two bugs
   `h264.decode`/`h264.transform` had JUST been fixed for upstream in this
   repo's own history (commit `14e72ea`, \"resolve luma Horizontal
   multi-macroblock CAVLC desync\": `blk->col-row` was a transposed/wrong
   block-index mapping, and `luma-dc-hadamard` was missing an internal
   transpose) — `h264.encode` was initially developed against a stale west
   pin predating that fix. After syncing to the fixed `h264.decode` (and
   fixing this encoder's OWN `dc-nc` derivation, which had the SAME
   `:dc-nnz`-vs-`:ac-nnz` bug the upstream fix corrected on the decode
   side — see `encode-macroblock!`'s `dc-nc` binding), ALL previously-
   failing cases — including the original complex multi-macroblock,
   real-AC, Horizontal-prediction test image — decode bit-exact via real
   ffmpeg. No known decode-side gap remains open as of this test suite."
  (:require [clojure.test :refer [deftest is testing]]
            [clojure.java.io :as io]
            [h264.encode :as encode]
            [h264.decode :as decode]))

(defn- rd [p] (mapv #(bit-and (int %) 0xff) (with-open [in (io/input-stream (io/resource p))] (.readAllBytes in))))

(defn- sad [a b] (reduce + (map (fn [x y] (let [d (- x y)] (if (neg? d) (- d) d))) a b)))

(deftest flat-single-mb-roundtrip-bit-exact
  (testing "a flat 16x16 macroblock round-trips bit-exact through our own decoder across a wide QP range (no AC residual is ever needed for a truly flat block, so this only exercises the DC path)"
    (doseq [qp [0 5 15 24 26 35 45 51]]
      (let [luma (vec (repeat 256 128))
            {:keys [bytes]} (encode/encode-idr-luma-frame {:width 16 :height 16 :qp qp :luma luma})
            result (decode/decode-idr-frame bytes)]
        (is (= luma (:luma result)) (str "qp=" qp))))))

(deftest flat-multi-mb-roundtrip-bit-exact
  (testing "a flat 32x32 (4 macroblock) picture round-trips bit-exact — exercises cross-macroblock DC-only prediction without needing AC"
    (let [luma (vec (repeat 1024 100))
          {:keys [bytes mb-states]} (encode/encode-idr-luma-frame {:width 32 :height 32 :qp 24 :luma luma})
          result (decode/decode-idr-frame bytes)]
      (is (= luma (:luma result)))
      (is (= 4 (count mb-states))))))

(deftest gradient-single-mb-roundtrip-bounded-error
  (testing "a single-macroblock horizontal gradient (real AC residual, CodedBlockPatternLuma=15) round-trips through our own decoder with small, bounded lossy-compression error"
    (let [luma (vec (for [_r (range 16) c (range 16)] (+ 100 (* c 2))))
          {:keys [bytes mb-states]} (encode/encode-idr-luma-frame {:width 16 :height 16 :qp 20 :luma luma})
          result (decode/decode-idr-frame bytes)]
      (is (some (fn [state] (some pos? (:ac-nnz state))) mb-states)
          "at least one AC block actually carries a nonzero coefficient (sanity: not silently degenerating to DC-only)")
      (is (< (sad luma (:luma result)) (* 256 3)) "average per-pixel error stays small (<3) at this QP"))))

(deftest gradient-multi-mb-roundtrip-bounded-error
  (testing "a 2x2-macroblock diagonal gradient (real AC + cross-macroblock neighbor derivation for both intra prediction and CAVLC nC, including the DC block's own nC) round-trips through our own decoder with bounded error"
    (let [luma (vec (for [r (range 32) c (range 32)] (mod (+ (* r 3) (* c 5) 50) 200)))
          {:keys [bytes mb-states]} (encode/encode-idr-luma-frame {:width 32 :height 32 :qp 24 :luma luma})
          result (decode/decode-idr-frame bytes)]
      (is (= 4 (count mb-states)))
      (is (< (/ (sad luma (:luma result)) 1024.0) 3.0) "average per-pixel error stays small"))))

(deftest mode-decision-chooses-among-available-modes
  (testing "mode decision picks DC for the first (no-neighbor) macroblock, and is free to pick DC/Vertical/Horizontal for later ones depending on which minimizes SAD"
    (let [luma (vec (repeat 256 128))
          {:keys [mb-states]} (encode/encode-idr-luma-frame {:width 16 :height 16 :qp 26 :luma luma})]
      (is (= 2 (:pred-mode (first mb-states))) "DC (mode 2) is the only legal choice with no neighbors"))))

(deftest cbp-luma-zero-for-flat-content
  (testing "CodedBlockPatternLuma degenerates to 0 (no AC blocks coded) for genuinely flat content, matching h264.decode/i16x16-mb-info's mb_type<=12 case"
    (let [luma (vec (repeat 256 128))
          {:keys [mb-states]} (encode/encode-idr-luma-frame {:width 16 :height 16 :qp 26 :luma luma})]
      (is (every? zero? (:ac-nnz (first mb-states)))))))

(deftest encode-mb0-realac-golden-vector
  (testing "encode-mb0-realac.h264 — a single Intra_16x16 macroblock with a real 16x16 horizontal gradient (100..130), giving REAL (nonzero) AC residual at QP 20, generated by `h264.encode`, and independently verified (2026-07, ffmpeg 8.1.1) to decode bit-exact via real ffmpeg:
       ffmpeg -i encode-mb0-realac.h264 -pix_fmt yuv420p out.yuv
     — producing exactly this fixture's `.ref.yuv`. Re-verifies the
     checked-in fixture decodes identically via THIS REPO's own decoder,
     demonstrating this encoder's output is a real, interoperable H.264
     Annex B bitstream."
    (let [bytes (rd "h264/fixtures/encode-mb0-realac.h264")
          ref (rd "h264/fixtures/encode-mb0-realac.ref.yuv")
          result (decode/decode-idr-frame bytes)
          luma-ref (vec (take 256 ref))]
      (is (= 16 (:width result)))
      (is (= 16 (:height result)))
      (is (= luma-ref (:luma result)) "bit-exact vs real ffmpeg's own decode of this exact file")
      (is (> (count (distinct (:luma result))) 1) "real AC content, not degenerate to flat"))))

