(deftest encode-multimb-realac-golden-vector
  (testing "encode-multimb-realac.h264 — a 32x32 (2x2 macroblock) picture with a real 2-D oscillating pattern (`sin`/`cos` of position, same style as this repo's own decode-side `chroma-multimb32.h264` fixture, but here applied to LUMA), giving REAL AC residual in EVERY macroblock plus real cross-macroblock CAVLC neighbor (nC) derivation for both AC blocks AND the Intra16x16 luma-DC block, and Horizontal Intra_16x16 prediction actually selected for 2 of the 4 macroblocks — generated by `h264.encode` at QP 24 and independently verified (2026-07, ffmpeg 8.1.1) to decode BIT-EXACT via real ffmpeg (zero pixel difference from ffmpeg's own decode of this exact file, and zero `corrupted macroblock`/`error while decoding` messages). This is the strongest interoperability evidence in this test suite — the exact combination (multi-MB + real AC + cross-MB nC + Horizontal prediction) that most exercises the encoder's quantizer, CAVLC writer, and neighbor bookkeeping together."
