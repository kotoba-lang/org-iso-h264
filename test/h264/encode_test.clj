(deftest chroma-defaults-to-flat-when-omitted
  (testing "omitting :cb/:cr from encode-idr-luma-frame's input preserves this fn's ORIGINAL luma-only behavior exactly (flat 128 chroma planes, zero residual, CodedBlockPatternChroma degenerates to 0) — backward compatibility for existing callers that never passed chroma."
    (let [luma (vec (repeat 256 128))
          {:keys [mb-states]} (encode/encode-idr-luma-frame {:width 16 :height 16 :qp 20 :luma luma})]
      (is (= 0 (:intra-chroma-pred-mode (first mb-states))))
      (is (every? zero? (:ac-nnz (:cb (first mb-states)))))
      (is (every? zero? (:ac-nnz (:cr (first mb-states)))))
      (is (= [128] (distinct (apply concat (:recon (:cb (first mb-states))))))))))

(deftest chroma-single-mb-roundtrip-bounded-error
  (testing "a single-macroblock real Cb/Cr gradient (distinct per-component content, real AC residual) round-trips through our own decoder with small bounded error, and picks a legal chroma mode"
    (let [luma (vec (repeat 256 128))
          cb (vec (for [_r (range 8) c (range 8)] (+ 80 (* c 6))))
          cr (vec (for [r (range 8) _c (range 8)] (+ 160 (* r 6))))
          {:keys [bytes mb-states]} (encode/encode-idr-luma-frame {:width 16 :height 16 :qp 20 :luma luma :cb cb :cr cr})
          result (decode/decode-idr-frame bytes)]
      (is (some pos? (:ac-nnz (:cb (first mb-states)))) "Cb actually carries real AC residual, not degenerate to DC-only")
      (is (some pos? (:ac-nnz (:cr (first mb-states)))) "Cr actually carries real AC residual, not degenerate to DC-only")
      (is (< (sad cb (:cb result)) (* 64 3)) "Cb average per-pixel error stays small at this QP")
      (is (< (sad cr (:cr result)) (* 64 3)) "Cr average per-pixel error stays small at this QP"))))

(deftest chroma-multimb-realac-roundtrip-bounded-error
  (testing "a 32x32 (2x2-macroblock) picture with a real 2-D oscillating Cb/Cr pattern (flat luma, so libx264-style RD reasoning would keep luma Intra_16x16 DC-only — mirrors this repo's own decode-side chroma-multimb32.h264 fixture's design) round-trips through our own decoder with bounded error, exercising real cross-macroblock chroma AC CAVLC neighbor derivation for all 4 macroblocks and more than one Intra_Chroma prediction mode"
    (let [luma (vec (repeat 1024 128))
          cb (vec (for [r (range 16) c (range 16)] (int (+ 128 (* 40 (Math/sin (/ c 2.0)) (Math/cos (/ r 3.0)))))))
          cr (vec (for [r (range 16) c (range 16)] (int (+ 128 (* 40 (Math/cos (/ c 3.0)) (Math/sin (/ r 2.0)))))))
          {:keys [bytes mb-states]} (encode/encode-idr-luma-frame {:width 32 :height 32 :qp 20 :luma luma :cb cb :cr cr})
          result (decode/decode-idr-frame bytes)]
      (is (every? (fn [s] (some pos? (:ac-nnz (:cb s)))) mb-states) "every macroblock's Cb carries real AC residual")
      (is (every? (fn [s] (some pos? (:ac-nnz (:cr s)))) mb-states) "every macroblock's Cr carries real AC residual")
      (is (> (count (distinct (map :intra-chroma-pred-mode mb-states))) 1)
          "more than one Intra_Chroma mode was actually selected across these 4 macroblocks")
      (is (< (/ (sad cb (:cb result)) 256.0) 3.0) "Cb average per-pixel error stays small")
      (is (< (/ (sad cr (:cr result)) 256.0) 3.0) "Cr average per-pixel error stays small"))))

